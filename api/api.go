package api

import (
	"context"
	"fmt"
	"log"
	"net/http"

	"github.com/gorilla/mux"
	"github.com/sergicanet9/go-mongo-restapi/adapters/handlers"
	"github.com/sergicanet9/go-mongo-restapi/adapters/repositories/postgres"
	"github.com/sergicanet9/go-mongo-restapi/async"
	"github.com/sergicanet9/go-mongo-restapi/config"
	"github.com/sergicanet9/go-mongo-restapi/core/services/user"
	_ "github.com/sergicanet9/go-mongo-restapi/docs" // docs is generated by Swag CLI, needs to be imported.
	infrastructure "github.com/sergicanet9/scv-go-framework/v2/infrastructure/postgres"
	httpSwagger "github.com/swaggo/http-swagger"
)

// API struct
type API struct {
	config config.Config
	router *mux.Router
}

// Initialize API
func (a *API) Initialize(ctx context.Context, cfg config.Config) {
	a.config = cfg

	router := mux.NewRouter()
	a.router = router

	a.router.PathPrefix("/swagger").Handler(
		httpSwagger.Handler(httpSwagger.URL(fmt.Sprintf("%s:%d/swagger/doc.json", a.config.Address, a.config.Port))),
	)

	// db, err := infrastructure.ConnectMongoDB(ctx, a.config.DBName, a.config.MongoConnectionString)
	db, err := infrastructure.ConnectPostgresDB(a.config.PostgresConnectionString)
	if err != nil {
		log.Fatal(err)
	}

	handlers.SetHealthRoutes(ctx, a.config, a.router)

	// userRepo := mongo.NewUserRepository(db.Collection(domain.CollectionNameUser), &domain.User{})
	// userService := user.NewUserService(a.config, userRepo)
	userRepo := postgres.NewUserRepository(db)
	userService := user.NewUserService(a.config, userRepo)
	handlers.SetUserRoutes(ctx, a.config, a.router, userService)

	if a.config.Async.Run {
		async := async.NewAsync(a.config)
		go async.Run(ctx)
	}
}

// Run API
func (a *API) Run() {
	log.Printf("Environment: %s", a.config.Environment)
	log.Printf("Listening on port %d", a.config.Port)
	log.Printf("Open %s:%d/swagger/index.html in the browser", a.config.Address, a.config.Port)
	log.Fatal(http.ListenAndServe(fmt.Sprintf(":%d", a.config.Port), a.router))
}
